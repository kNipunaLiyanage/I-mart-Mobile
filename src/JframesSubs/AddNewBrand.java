    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            JOptionPane.showMessageDialog(this, "<html>Select Valid Row To Remove Details..</html>", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            DefaultTableModel defaultTableModel = (DefaultTableModel) jTable1.getModel();
            int i2 = JOptionPane.showConfirmDialog(this, "<html>Are you sure you want to remove this Brand?..</html>", "Warning", JOptionPane.WARNING_MESSAGE);
            if (i2 == 0) {
                String brandname = defaultTableModel.getValueAt(i, 0).toString();
                try {
                    DB.iud("delete from brands where name = '" + brandname + "'");
                    JOptionPane.showMessageDialog(this, "<html>Brand Removed!..</html>", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    Thread.sleep(500);
                    new AddNewBrand().setVisible(true);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

