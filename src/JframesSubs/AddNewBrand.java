    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String repairmanname = jTextField1.getText();
            if (repairmanname.equals("")) {
                JOptionPane.showMessageDialog(this, "<html>Enter brand Name!..</html>", "Error", JOptionPane.ERROR_MESSAGE);

            } else {
                ResultSet rs = DB.search("select * from brands where name= '" + repairmanname + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "<html>Brand already exists!..</html>", "Error", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                    jTextField1.grabFocus();

                } else {
                    DB.iud("INSERT INTO `imartmobile`.`brands`\n"
                            + "            (`name`)\n"
                            + "VALUES ('" + repairmanname + "');");

                    JOptionPane.showMessageDialog(this, "<html>New Brand Added!..</html>", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    Thread.sleep(500);
                    new AddNewBrand().setVisible(true);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

