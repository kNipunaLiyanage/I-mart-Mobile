            String cuscontact = jTextField5.getText();
            String fault = jTextField6.getText();
            String description = jTextField7.getText();

            Date d2 = new Date();
            SimpleDateFormat format2 = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat format3 = new SimpleDateFormat("hh:mm:ss");
